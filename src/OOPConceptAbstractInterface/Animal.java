package OOPConceptAbstractInterface;

public abstract class Animal {
	
	public abstract void cat();
	
	public abstract void horse();
	
	public void dog() {
		
		System.out.println("I have a dog, name is tomy");
	}
	
	public void mouse() {
		
		System.out.println("I have a mouse, name is poty");
	}


}
